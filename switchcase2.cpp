#include<stdio.h>
int main()
{
	char c;
	printf("enter any alphabet\n");
	scanf("%c",&c);
	switch(c)
	{
	case 'A' :
		case 'E' :
			case 'I' :
				case 'O' :
					case 'U' :
						case 'a' :
							case 'e' :
								case 'i' :
									case 'o' :
										case 'u' :
											printf("it is vowel\n");
											break;
											default :
											printf("it is consonant\n");
											break;
    }
										return 0;
			
}
